package sist.com.Practice;

public class VariableP1 {

	public static void main(String[] args) {
		
		boolean question = true;   // boolean타입의 question이라는 이름의 변수에 true를 저장.
		
		char word = 'A';           // char 타입의 word라는 이름의 변수에 문자 A의 아스키값 65를 저장.
		
		byte b = 10;               // byte 타입의 b라는 이름의 변수에 10을 저장.
		short s = 25;              // short 타입의 s라는 이름의 변수에 25를 저장.
		int num = 10;              // int 타입의 num이라는 이름의 변수에 10을 저장.
		long l = 50;               // long 타입의 l이라는 이름의 변수에 50을 저장.
		
		float f = 135.5F;          // 실수의 기본타입은 double이므로 반드시 옆에 F를 붙여야한다.
		double d = 368.5;          // double 타입의 d라는 이름의 변수에 368.5를 저장.
		
		
		System.out.println(question);
		System.out.println(word);
		System.out.println(b);
		System.out.println(s);
		System.out.println(num);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		
	}

}
